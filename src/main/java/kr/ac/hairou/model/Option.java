package kr.ac.hairou.model;

public class Option {
	private int code;
	private int mepisode;
	private int oepisode;
	private String action;
	
	public int getCode() {
		return code;
	}
	public void setCode(int code) {
		this.code = code;
	}
	public int getMepisode() {
		return mepisode;
	}
	public void setMepisode(int mepisode) {
		this.mepisode = mepisode;
	}
	public int getOepisode() {
		return oepisode;
	}
	public void setOepisode(int oepisode) {
		this.oepisode = oepisode;
	}
	public String getAction() {
		return action;
	}
	public void setAction(String action) {
		this.action = action;
	}
}
